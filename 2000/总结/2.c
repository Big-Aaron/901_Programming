#include <stdio.h>

int main() {
    char f[] = "%10.10s\n";
    char a[] = "***************";

    for (int i = 1; i <= 4; ++i) {
        *(f + 2) = 2 * i + '0';
        *(f + 4) = 4 * i / 10 + '0';
        *(f + 5) = 4 * i % 10 + '0';
        printf(f, a);
    }

}
